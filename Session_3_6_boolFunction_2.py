# Using eval to convert boolean values
print('eval("True"):', eval("True"))
print('eval("False"):', eval("False"))

# Using bool to convert boolean values
print('bool(0):', bool(0))
print('bool(1):', bool(1))
print('bool(123):', bool(123))
print('bool(""):', bool(""))
print('bool("False"):', bool("False"))
print('bool("1-1"):', bool("1-1"))
print('bool(1-1):', bool(1-1))
